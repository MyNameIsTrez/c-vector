int	main(void)
{
	int	*ints;
	int	v;

	ints = vector_new(sizeof(int));

	vector_reserve(&ints, 1);

	v = 4;
	vector_push(&ints, &v);
	v = 2;
	vector_push(&ints, &v);
	v = 0;
	vector_push(&ints, &v);

	printf("%d%d%d\n", ints[0], ints[1], ints[2]);

	return (EXIT_SUCCESS);
}

